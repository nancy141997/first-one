<font size="6"><u>Assignment Operator ( = )</u></font><br/>
Operates '=' is used for assignment, it takes the right-hand side (called rvalue) and copy it into the left-hand side (called lvalue). Assignment operator is the only operator which can be overloaded but cannot be inherited.<br/>

<font size="6"><u>Mathematical Operators</u></font><br/>
There are operators used to perform basic mathematical operations. Addition (+) , subtraction (-) , diversion (/) multiplication (*) and modulus (%) are the basic mathematical operators. Modulus operator cannot be used with floating-point numbers.<br/>

C++ and C also use a shorthand notation to perform an operation and assignment at same type. <br/>
