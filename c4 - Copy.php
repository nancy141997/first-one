<font size="6"><u>Object Oriented Programming</u></font><br/>
<font size="5">Object Oriented programming is a programming style that is associated with the concept of Class, Objects and various other concepts revolving around these two, like Inheritance, Polymorphism, Abstraction, Encapsulation etc.</font><br/>
<center><img src="oops1.png" height="400"></center>
<font size="6"><u>Class</u></font><br/>
<font size="5">Here we can take Human Being as a class. A class is a blueprint for any functional entity which defines its properties and its functions. Like Human Being, having body parts, and performing various actions.</font><br/>

<font size="6"><u>Inheritance</u></font><br/>
<font size="5">Considering HumanBeing a class, which has properties like hands, legs, eyes etc, and functions like walk, talk, eat, see etc. Male and Female are also classes, but most of the properties and functions are included in HumanBeing, hence they can inherit everything from class HumanBeing using the concept of Inheritance.</font><br/>

<font size="6"><u>Objects</u></font><br/>
<font size="5">My name is Abhishek, and I am an instance/object of class Male. When we say, Human Being, Male or Female, we just mean a kind, you, your friend, me we are the forms of these classes. We have a physical existence while a class is just a logical definition. We are the objects.</font><br/>

<font size="6"><u>Abstraction</u></font><br/>
<font size="5">Abstraction means, showcasing only the required things to the outside world while hiding the details. Continuing our example, Human Being's can talk, walk, hear, eat, but the details are hidden from the outside world. We can take our skin as the Abstraction factor in our case, hiding the inside mechanism.</font><br/>

<font size="6"><u>Encapsulation</u></font><br/>
<font size="5">This concept is a little tricky to explain with our example. Our Legs are binded to help us walk. Our hands, help us hold things. This binding of the properties to functions is called Encapsulation.</font><br/>

<font size="6"><u>Polymorphism</u></font><br/>
<font size="5">Polymorphism is a concept, which allows us to redefine the way something works, by either changing how it is done or by changing the parts using which it is done. Both the ways have different terms for them.<br/>
<br/>
If we walk using our hands, and not legs, here we will change the parts used to perform something. Hence this is called Overloading.<br/>

And if there is a defined way of walking, but I wish to walk differently, but using my legs, like everyone else. Then I can walk like I want, this will be called as Overriding.</font><br/>


